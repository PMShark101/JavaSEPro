package com.pmshark.define.variable;

public class VariableDemo1 {
    public static void main(String[] args) {
        int age = 23;
        System.out.println(age);

        double score = 15.9;
        System.out.println(score);

        System.out.println("-------------------------");

        int num = 88;
        System.out.println(num);
        System.out.println(num);
        System.out.println(num);
        System.out.println(num);
        System.out.println(num);
        System.out.println(num);

        System.out.println("-------------------------");
        int age2 = 28;
        System.out.println(age2);
        age2 = 29;
        System.out.println(age2);
        age2 = age2 + 1;
        System.out.println(age2);

        System.out.println("-------------------------");
        double money = 9.5;
        System.out.println(money);

        money += 10;
        System.out.println(money);

        money -= 5;
        System.out.println(money);

    }
}
